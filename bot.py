def send_telegram_notification(site, article, backlink_data, settings):
    """Send article publish notification + backlink content summary to Telegram."""
    tg = settings.get("telegram", {})
    if not tg.get("enabled") or not tg.get("bot_token"):
        return
    token = tg["bot_token"]
    chat_ids = tg.get("allowed_chat_ids", [])
    if not chat_ids:
        return

    url = f"https://{site['domain']}/{article['slug']}/"
    date = article.get("date", datetime.now().strftime("%B %d, %Y"))

    reddit_subs = ""
    quora_q = ""
    if backlink_data:
        lines = backlink_data.split("\n")
        for i, line in enumerate(lines):
            if "**Post to:**" in line:
                reddit_subs = line.replace("**Post to:**", "").strip()
            if "**Answer this question:**" in line:
                quora_q = lines[i].replace("**Answer this question:**", "").strip()[:80]

    repo = site.get("repo", "")
    slug = article.get("slug", "")
    github_link = f'https://github.com/{repo}/blob/main/backlinks/{slug}.md' if repo else ""

    text = (
        f"📰 <b>New article published</b>\n\n"
        f"🌐 <b>{site['domain']}</b>\n"
        f"📌 {article['title']}\n"
        f"🔗 {url}\n"
        f"📅 {date}\n"
        f"✍️ {article.get('author', '')}\n"
    )
    if reddit_subs or quora_q:
        text += f"\n📣 <b>Backlink content ready:</b>\n"
        if reddit_subs:
            text += f"Reddit → {reddit_subs}\n"
        text += f"LinkedIn → post ready\n"
        if quora_q:
            text += f'Quora → "{quora_q}"\n'
        if github_link:
            text += f'\n📂 <a href="{github_link}">Open backlink content</a>'
        else:
            text += f"\nCheck <code>backlinks/{article['slug']}.md</code> in repo to copy and post."

    for chat_id in chat_ids:
        try:
            requests.post(
                f"https://api.telegram.org/bot{token}/sendMessage",
                json={"chat_id": chat_id, "text": text, "parse_mode": "HTML", "disable_web_page_preview": True},
                timeout=10,
            )
        except Exception as e:
            print(f"  Telegram notify failed: {e}")


