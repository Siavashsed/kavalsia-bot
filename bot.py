def search_web(topic, web_cfg):
    """
    Ground article generation with live search results.
    Supports Tavily (recommended) and Perplexity.
    Returns a string of recent context snippets or "" if disabled/failed.
    """
    if not web_cfg or not web_cfg.get("enabled"):
        return ""

    provider = web_cfg.get("provider", "tavily")

    if provider == "tavily":
        key = web_cfg.get("tavily_api_key") or os.environ.get("TAVILY_API_KEY", "")
        if not key:
            return ""
        try:
            r = requests.post("https://api.tavily.com/search", json={
                "api_key": key,
                "query": topic,
                "search_depth": "basic",
                "max_results": 5,
                "include_answer": True,
            }, timeout=20)
            data = r.json()
            snippets = [f"- {res['title']}: {res.get('content','')[:220]}"
                        for res in data.get("results", [])]
            answer = data.get("answer", "")
            context = (f"Summary: {answer}\n" if answer else "") + "\n".join(snippets)
            print(f"  Web search (Tavily): {len(snippets)} results")
            return context
        except Exception as e:
            print(f"  Web search error: {e}")
            return ""

    elif provider == "perplexity":
        key = web_cfg.get("perplexity_api_key") or os.environ.get("PERPLEXITY_API_KEY", "")
        if not key:
            return ""
        try:
            r = requests.post("https://api.perplexity.ai/chat/completions", json={
                "model": "sonar",
                "messages": [{"role": "user", "content": f"Find recent facts and news about: {topic}. Return key facts, statistics, and recent developments in 3-5 bullet points."}],
            }, headers={"Authorization": f"Bearer {key}"}, timeout=20)
            content = r.json()["choices"][0]["message"]["content"]
            print(f"  Web search (Perplexity): context retrieved")
            return content
        except Exception as e:
            print(f"  Web search error (Perplexity): {e}")
            return ""

    return ""


def get_internal_link_candidates(articles, topic, n=3):
    """
    Find the most relevant older articles on the same site for internal linking.
    Builds topical authority clusters - pure SEO value, not link spam.
    """
    if not articles or len(articles) < 2:
        return []
    topic_words = set(re.findall(r'\w+', topic.lower())) - {'the','a','an','and','or','in','on','of','to','for','is','are','how','why','what'}
    scored = []
    for a in articles[:-1]:  # exclude the most recent (current article not yet in list)
        title_words = set(re.findall(r'\w+', a.get("title","").lower()))
        score = len(topic_words & title_words)
        if score > 0:
            scored.append((score, a))
    scored.sort(key=lambda x: -x[0])
    return [a for _, a in scored[:n]]


def insert_internal_links(html, articles, site_domain, current_slug=""):
    """
    Weave 2-3 contextual internal links into existing paragraph text.
    Links are inserted naturally by appending a short contextual phrase to a
    suitable paragraph - NOT as a separate related-articles block.
    Uses relative URLs: ../{slug}/ format.
    Only runs when there are at least 3 other articles to choose from.
    """
    STOP_WORDS = {
        'the', 'a', 'an', 'is', 'are', 'how', 'why', 'what', 'for', 'to',
        'in', 'of', 'and', 'or', 'with', 'vs', 'on', 'at', 'by', 'as',
        'be', 'do', 'if', 'it', 'no', 'not', 'so', 'up', 'we', 'my',
        'our', 'you', 'was', 'has', 'had', 'can', 'but', 'from', 'this',
        'that', 'your', 'than', 'more', 'into', 'over', 'also', 'about',
    }

    # Filter candidates: exclude current article, require at least 3 others
    candidates = [a for a in articles if a.get("slug") and a.get("slug") != current_slug and a.get("title")]
    if len(candidates) < 3:
        return html

    # Score candidates by word overlap with the article title extracted from HTML
    title_match = re.search(r'<title[^>]*>([^<]+)</title>', html, re.IGNORECASE)
    article_title = title_match.group(1) if title_match else ""
    # Strip site name suffix like " | Site Name"
    article_title = re.sub(r'\s*[|]\s*.+$', '', article_title).strip()
    current_words = set(re.findall(r'\w+', article_title.lower())) - STOP_WORDS

    scored = []
    for a in candidates:
        title_words = set(re.findall(r'\w+', a["title"].lower())) - STOP_WORDS
        overlap = len(current_words & title_words)
        if overlap >= 2:
            scored.append((overlap, a))
    scored.sort(key=lambda x: -x[0])
    targets = [a for _, a in scored[:3]]

    if not targets:
        return html

    # Find <p> tags in the article body (skip very short ones and the first paragraph)
    # Split HTML into parts to safely work on text nodes inside <p> tags
    p_pattern = re.compile(r'(<p(?:\s[^>]*)?>)(.*?)(</p>)', re.DOTALL | re.IGNORECASE)
    paragraphs = [(m.start(), m.end(), m.group(1), m.group(2), m.group(3))
                  for m in p_pattern.finditer(html)]

    # Skip the first paragraph (usually the lede), filter out short/empty ones
    usable = [p for p in paragraphs[1:] if len(re.sub(r'<[^>]+>', '', p[3]).strip()) > 80]

    if not usable:
        return html

    # Spread links across different paragraphs
    import random as _random
    # Pick evenly-spaced paragraphs from the usable set
    step = max(1, len(usable) // (len(targets) + 1))
    chosen_indices = [min(step * (i + 1), len(usable) - 1) for i in range(len(targets))]
    # Deduplicate indices
    chosen_indices = list(dict.fromkeys(chosen_indices))

    # Build a map of paragraph start positions to inject links
    inject_map = {}
    for idx, article in zip(chosen_indices, targets):
        if idx < len(usable):
            para = usable[idx]
            inject_map[para[0]] = (para, article)

    if not inject_map:
        return html

    # Rebuild HTML with links injected
    result_parts = []
    prev_end = 0
    for m in p_pattern.finditer(html):
        if m.start() not in inject_map:
            result_parts.append(html[prev_end:m.end()])
            prev_end = m.end()
            continue
        para_tuple, target_article = inject_map[m.start()]
        open_tag, inner, close_tag = m.group(1), m.group(2), m.group(3)
        link_url = f"../{target_article['slug']}/"
        link_title = target_article['title']
        # Append link as a natural sentence within the paragraph
        link_phrase = f' For more on this, see <a href="{link_url}">{link_title}</a>.'
        # Insert before the closing </p>
        new_para = f"{open_tag}{inner}{link_phrase}{close_tag}"
        result_parts.append(html[prev_end:m.start()])
        result_parts.append(new_para)
        prev_end = m.end()

    result_parts.append(html[prev_end:])
    return ''.join(result_parts)


def inject_affiliate_links(html, affiliate_dict):
    """
    Wrap first occurrence of each keyword in affiliate link.
    Operates on text nodes only - never injects inside existing tags or attributes.
    Keywords are sorted longest-first to prevent partial matches.
    """
    if not affiliate_dict:
        return html

    for keyword, url in sorted(affiliate_dict.items(), key=lambda x: -len(x[0])):
        if not keyword.strip() or not url.strip():
            continue
        # Split HTML by tags, only process text nodes
        parts = re.split(r'(<[^>]+>)', html)
        replaced = False
        for i, part in enumerate(parts):
            if part.startswith('<') or replaced:
                continue
            new_part = re.sub(
                rf'(?i)\b({re.escape(keyword)})\b',
                f'<a href="{url}" rel="sponsored noopener" target="_blank">\\1</a>',
                part, count=1
            )
            if new_part != part:
                parts[i] = new_part
                replaced = True
        html = ''.join(parts)
    return html


def broadcast_social(site, article, social_cfg):
    """
    Broadcast new article to social media.
    Supports: Make.com / Zapier webhook, Twitter/X API v2, LinkedIn API.
    """
    if not social_cfg or not social_cfg.get("enabled"):
        return

    domain  = site["domain"]
    url     = f"https://{domain}/{article['slug']}/"
    title   = article["title"]
    desc    = article.get("meta_description", "")
    image   = article.get("image", "")
    author  = article.get("author", "")

    # ── Webhook (Make.com / Zapier / n8n) ────────────────────────────────────
    webhook = social_cfg.get("webhook_url", "").strip()
    if webhook:
        try:
            r = requests.post(webhook, json={
                "title": title, "url": url, "description": desc,
                "image": image, "domain": domain, "author": author,
                "category": site.get("category", ""),
            }, timeout=15)
            print(f"  Social webhook: {r.status_code} → {domain}")
        except Exception as e:
            print(f"  Social webhook error: {e}")

    # ── Twitter / X API v2 ───────────────────────────────────────────────────
    # NOTE: Posting tweets requires OAuth 1.0a (consumer key + access token),
    # not a Bearer token (which is app-only / read-only).
    # Use twitter_oauth_consumer_key, twitter_oauth_consumer_secret,
    # twitter_oauth_token, and twitter_oauth_token_secret in your config,
    # and sign requests with requests-oauthlib or tweepy.
    # A Bearer token will always return 403 Forbidden on write endpoints.
    if social_cfg.get("twitter_enabled"):
        tw_consumer_key    = social_cfg.get("twitter_oauth_consumer_key", "")
        tw_consumer_secret = social_cfg.get("twitter_oauth_consumer_secret", "")
        tw_token           = social_cfg.get("twitter_oauth_token", "")
        tw_token_secret    = social_cfg.get("twitter_oauth_token_secret", "")
        if tw_consumer_key and tw_consumer_secret and tw_token and tw_token_secret:
            try:
                from requests_oauthlib import OAuth1
                tweet = f"{title}\n\n{desc[:100]}...\n\n🔗 {url}"[:280]
                auth = OAuth1(tw_consumer_key, tw_consumer_secret, tw_token, tw_token_secret)
                r = requests.post(
                    "https://api.twitter.com/2/tweets",
                    auth=auth,
                    headers={"Content-Type": "application/json"},
                    json={"text": tweet},
                    timeout=15,
                )
                print(f"  Twitter/X: {r.status_code}")
                if r.status_code not in (200, 201):
                    print(f"  Twitter/X error body: {r.text[:200]}")
            except ImportError:
                print("  Twitter/X: install requests-oauthlib (pip install requests-oauthlib)")
            except Exception as e:
                print(f"  Twitter error: {e}")
        else:
            print("  Twitter/X: skipped - set twitter_oauth_consumer_key/secret + twitter_oauth_token/secret in social_media config (Bearer token is read-only and cannot post)")

    # ── LinkedIn API ─────────────────────────────────────────────────────────
    if social_cfg.get("linkedin_enabled") and social_cfg.get("linkedin_token") and social_cfg.get("linkedin_org_id"):
        try:
            share_text = f"{title}\n\n{desc}\n\nRead more: {url}"
            r = requests.post(
                "https://api.linkedin.com/v2/shares",
                headers={
                    "Authorization": f"Bearer {social_cfg['linkedin_token']}",
                    "Content-Type": "application/json",
                },
                json={
                    "owner": f"urn:li:organization:{social_cfg['linkedin_org_id']}",
                    "subject": title,
                    "text": {"text": share_text},
                    "content": {"contentEntities": [{"entityLocation": url}], "title": title},
                    "distribution": {"linkedInDistributionTarget": {"visibleToGuest": True}},
                },
                timeout=15,
            )
            print(f"  LinkedIn: {r.status_code}")
        except Exception as e:
            print(f"  LinkedIn error: {e}")


def ping_google_indexing(url, indexing_cfg):
    """
    Notify Google Indexing API the moment a new page is published.
    Requires a Google service account JSON with Indexing API access.
    The service account must be added as Owner in Google Search Console.
    """
    if not indexing_cfg or not indexing_cfg.get("enabled"):
        return
    sa_path = indexing_cfg.get("service_account_path", "google-service-account.json")
    if not os.path.exists(sa_path):
        print(f"  Google Indexing: service account file not found ({sa_path})")
        return
    try:
        from google.oauth2 import service_account
        import google.auth.transport.requests as ga_requests
        creds = service_account.Credentials.from_service_account_file(
            sa_path, scopes=["https://www.googleapis.com/auth/indexing"]
        )
        creds.refresh(ga_requests.Request())
        r = requests.post(
            "https://indexing.googleapis.com/v3/urlNotifications:publish",
            headers={"Authorization": f"Bearer {creds.token}"},
            json={"url": url, "type": "URL_UPDATED"},
            timeout=10,
        )
        if r.status_code == 200:
            print(f"  Google Indexing: ✓ pinged {url}")
        else:
            print(f"  Google Indexing: {r.status_code} {r.text[:80]}")
    except ImportError:
        print("  Google Indexing: install google-auth (pip install google-auth)")
    except Exception as e:
        print(f"  Google Indexing error: {e}")


