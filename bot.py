def _hex_to_rgb(hex_color):
    """Convert #rrggbb to 'r,g,b' string for rgba()."""
    h = hex_color.lstrip('#')
    if len(h) == 3:
        h = ''.join(c*2 for c in h)
    try:
        r, g, b = int(h[0:2],16), int(h[2:4],16), int(h[4:6],16)
        return f"{r},{g},{b}"
    except Exception:
        return "0,0,0"


