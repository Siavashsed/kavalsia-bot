def generate_sitemap(site, articles):
    domain = site["domain"]
    today  = datetime.now().strftime("%Y-%m-%d")
    url_entries = [
        f'  <url><loc>https://{domain}/</loc><lastmod>{today}</lastmod><changefreq>daily</changefreq><priority>1.0</priority></url>',
        f'  <url><loc>https://{domain}/about/</loc><lastmod>{today}</lastmod><changefreq>yearly</changefreq><priority>0.4</priority></url>',
        f'  <url><loc>https://{domain}/contact/</loc><lastmod>{today}</lastmod><changefreq>yearly</changefreq><priority>0.3</priority></url>',
        f'  <url><loc>https://{domain}/privacy/</loc><lastmod>{today}</lastmod><changefreq>yearly</changefreq><priority>0.2</priority></url>',
    ]
    for a in articles:
        lastmod = a.get("date_iso") or ""
        if not lastmod and a.get("date"):
            try:
                lastmod = datetime.strptime(a["date"], "%B %d, %Y").strftime("%Y-%m-%d")
            except Exception:
                lastmod = today
        if not lastmod:
            lastmod = today
        slug = a.get("slug", "")
        if not slug:
            continue
        url_entries.append(
            f'  <url><loc>https://{domain}/{slug}/</loc>'
            f'<lastmod>{lastmod}</lastmod>'
            f'<changefreq>monthly</changefreq>'
            f'<priority>0.8</priority></url>'
        )
    return (
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n'
        + "\n".join(url_entries)
        + "\n</urlset>"
    )


