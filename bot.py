def should_post_today(site, articles):
    """
    Frequency + warming gate. Returns (ok: bool, reason: str).
    Checks posts_per_week cap first, then warming ramp-up schedule.
    """
    # posts_per_week: enforce a minimum gap between posts (1-6/week)
    ppw = site.get("posts_per_week")
    if ppw and int(ppw) < 7:
        ppw = max(1, min(6, int(ppw)))
        min_gap = max(1, round(7 / ppw))
        if articles:
            dates = []
            for a in articles:
                iso = a.get("date_iso", "")
                if iso:
                    try:
                        dates.append(datetime.strptime(iso, "%Y-%m-%d"))
                    except ValueError:
                        pass
            if dates:
                last = max(dates)
                gap = (datetime.now() - last).days
                if gap < min_gap:
                    return False, f"Frequency cap ({ppw}/week): last post {gap}d ago, min gap {min_gap}d"

    warming = site.get("warming", {})
    if not warming.get("enabled", False):
        return True, ""

    started_str = warming.get("started", "")
    if not started_str:
        return True, ""

    try:
        started = datetime.strptime(started_str, "%Y-%m-%d")
    except ValueError:
        return True, ""

    days_live = (datetime.now() - started).days

    # Warming schedule: new → gradual → full speed
    # Days 0-13:  post every 3 days (trust-building phase)
    # Days 14-29: post every 2 days (establishing phase)
    # Days 30-59: post every 1 day (growth phase)
    # Days 60+:   no restriction (full speed)
    if days_live >= 60:
        return True, ""
    elif days_live >= 30:
        min_gap = 1
        phase = "growth"
    elif days_live >= 14:
        min_gap = 2
        phase = "establishing"
    else:
        min_gap = 3
        phase = "trust-building"

    if articles:
        dates = []
        for a in articles:
            iso = a.get("date_iso", "")
            if iso:
                try:
                    dates.append(datetime.strptime(iso, "%Y-%m-%d"))
                except ValueError:
                    pass
        if dates:
            last = max(dates)
            gap  = (datetime.now() - last).days
            if gap < min_gap:
                return False, f"Warming ({phase}, day {days_live}): last post {gap}d ago, min gap {min_gap}d"

    return True, ""


