def generate_backlink_content(article, site, client):
    """
    For each published article, generate ready-to-post content for
    Reddit, LinkedIn, and Quora - saved to backlinks/{slug}.md in the repo.
    These create real backlinks from high-DA platforms without cross-linking
    your own network sites to each other.
    """
    url      = f"https://{site['domain']}/{article['slug']}/"
    title    = article["title"]
    intro    = re.sub(r"<[^>]+>", "", article.get("intro", ""))[:400]
    persona  = site.get("persona", "an expert")
    category = site.get("category", "finance")

    prompt = f"""You are helping build genuine backlinks for an article. Generate ready-to-post content for three platforms.

Article title: {title}
Article URL: {url}
Site category: {category}
Author persona: {persona}
Article intro: {intro}

Return ONLY valid JSON - no markdown, no extra text:
{{
  "reddit": {{
    "subreddits": ["3 relevant subreddits without r/ prefix, pick based on the category"],
    "title": "Reddit post title - should be a question or insight, not the article title",
    "body": "Reddit post body, 150-200 words. Write as a genuine community member sharing an insight. Include the URL naturally at the end as 'Full breakdown here: {url}'. No self-promotion tone."
  }},
  "linkedin": {{
    "hook": "First line - a bold statement or surprising fact (under 20 words, no emoji)",
    "body": "LinkedIn post body, 120-150 words. 3-4 short paragraphs. Share a genuine insight from the article. End with: Read the full analysis → {url}",
    "hashtags": ["5 relevant hashtags without # prefix"]
  }},
  "quora": {{
    "question": "A Quora question this article answers directly",
    "answer": "Quora answer, 250-300 words. Answer the question genuinely and thoroughly. Reference the article once naturally mid-answer or at the end as additional reading, not as the main point. Write as {persona.split(',')[0].strip()}."
  }}
}}"""

    try:
        response = _retry(lambda: client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=2000,
            messages=[{"role": "user", "content": prompt}]
        ))
        text = response.content[0].text.strip()
        text = re.sub(r'^```json\s*', '', text)
        text = re.sub(r'\s*```$', '', text)
        data = json.loads(text)
    except Exception as e:
        print(f"  Backlink content generation failed: {e}")
        return None

    reddit  = data.get("reddit", {})
    linkedin = data.get("linkedin", {})
    quora   = data.get("quora", {})

    subs = ", ".join([f"r/{s}" for s in reddit.get("subreddits", [])])
    tags = " ".join([f"#{h}" for h in linkedin.get("hashtags", [])])

    md = f"""# Backlink content - {title}
**URL:** {url}
**Generated:** {datetime.now().strftime("%Y-%m-%d")}

---

## REDDIT
**Post to:** {subs}
**Title:** {reddit.get("title", "")}

{reddit.get("body", "")}

---

## LINKEDIN
**Hook:** {linkedin.get("hook", "")}

{linkedin.get("body", "")}

{tags}

---

## QUORA
**Answer this question:** {quora.get("question", "")}

{quora.get("answer", "")}
"""
    return md


def push_backlink_content(article, site, client, github_token, settings=None):
    slug = article.get("slug", "")
    if not slug:
        return
    md = generate_backlink_content(article, site, client)
    if not md:
        return
    path = f"backlinks/{slug}.md"
    pushed = _retry(lambda: github_push(site["repo"], path, md, f"Backlinks: {article['title'][:50]}", github_token))
    if pushed:
        print(f"  Backlink content saved → /{path}")
    if settings:
        send_telegram_notification(site, article, md, settings)


